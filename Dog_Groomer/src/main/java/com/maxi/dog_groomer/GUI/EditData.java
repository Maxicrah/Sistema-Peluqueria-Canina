    private void loadDatas(int num_cliente) {
        this.pet = control.traerPet(num_cliente);
        
        txtNombre.setText(pet.getName());
        txtRaza.setText(pet.getBreed());
        txtColor.setText(pet.getColor());
        txtObs.setText(pet.getObservation());
        txtNomDuenio.setText(pet.getOneOwner().getName());
        txtCelDuenio.setText(pet.getOneOwner().getTelOwner());
        
        if(pet.getAlergic().equals("SI")){
            cmbAlergico.setSelectedIndex(1);
        }
        else{
            if(pet.getAlergic().equals("NO")){
                cmbAlergico.setSelectedIndex(2);
            }
        }
        
        if(pet.getSpecial_attention().equals("SI")){
            cmbAtEsp.setSelectedIndex(1);
        }
        else{
            if(pet.getSpecial_attention().equals("NO")){
                cmbAtEsp.setSelectedIndex(2);
            }
        }
        
        
        
    }
