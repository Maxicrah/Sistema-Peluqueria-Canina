    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       
        //Datos mascota
        String namePet = txtNombre.getText();
        String breed = txtRaza.getText();
        String color = txtColor.getText();
        String obs = txtObs.getText();
        String nameOwner = txtNomDuenio.getText();
        String telOwner = txtCelDuenio.getText();
        String alergic = (String) cmbAlergico.getSelectedItem();
        String specialAttention = (String) cmbAtEsp.getSelectedItem();
        
        control.editPet(pet,namePet,breed,color,obs,nameOwner,telOwner,alergic,specialAttention);
        
        //mensaje finalizacion
        mostrarMensaje("Cambios realizados correctamente","Info","Edicion correcta");
        
        ViewPet pantalla = new ViewPet();
        pantalla.setVisible(true);
        pantalla.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

    public void mostrarMensaje(String mensaje,String tipoMensaje,String titulo){
                JOptionPane optionPane = new JOptionPane(mensaje);
                if(tipoMensaje.equals("Info")){
                    optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);}
                else if(tipoMensaje.equals("Error")){
                    optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
                }
                JDialog dialog = optionPane.createDialog(titulo);
                dialog.setAlwaysOnTop(true);
                dialog.setVisible(true);
    }

